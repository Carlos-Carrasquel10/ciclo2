    private void tbEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEmpleadosMouseClicked

        if (evt.getClickCount() == 1) { //cuando solo de un click atraves de un receptor va a capturar 
            //toda la informacion Jtable

            JTable receptor = (JTable) evt.getSource();

            txtId.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(), 0).toString());
            txtNombre.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(), 1).toString());
            txtCorreo.setText(receptor.getModel().getValueAt(receptor.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_tbEmpleadosMouseClicked

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        conexion objConexion = new conexion(); //obejto para instanciar la base de datos y interfaz grafica      

        empleadosBL oEmpleados = recuperarDatosGUI();

        String strSentenciaEliminar = String.format("DELETE FROM Empleados WHERE ID = %d", oEmpleados.getId());

        objConexion.ejecutarSentenciaSQL(strSentenciaEliminar);
        
        this.mostrarDatos();


    }//GEN-LAST:event_btnBorrarActionPerformed
    public empleadosBL recuperarDatosGUI() {

        empleadosBL oEmpleados = new empleadosBL();

        int id = (txtId.getText().isEmpty()) ? 0 : Integer.parseInt(txtId.getText());
        oEmpleados.setId(id);
        oEmpleados.setNombre(txtNombre.getText());
        oEmpleados.setCorreo(txtCorreo.getText());

        return oEmpleados;
    }

