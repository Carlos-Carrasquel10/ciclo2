    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        conexion objConexion = new conexion(); //obejto para instanciar la base de datos y interfaz grafica      

        empleadosBL oEmpleados = recuperarDatosGUI();

        String strSentenciaInsertar = String.format("INSERT INTO Empleados (id, nombre, correo )"
                + "VALUES (NULL, '%s', '%s');", oEmpleados.getNombre(), oEmpleados.getCorreo());

        objConexion.ejecutarSentenciaSQL(strSentenciaInsertar);

    }//GEN-LAST:event_btnAgregarActionPerformed
    public void mostrarDatos() {
        
        while(model.getRowCount() > 0){
        
            model.removeRow(0);
        }
        
        conexion objConexion = new conexion(); //obejto para instanciar la base de datos y interfaz grafica      
        try {
            
            ResultSet resultado = objConexion.consultarRegistros("SELECT * FROM Empleados ;");

            while (resultado.next()) { //next hace mostrar uno por uno

                System.out.println(resultado.getString("id"));
                System.out.println(resultado.getString("nombre"));
                System.out.println(resultado.getString("correo"));
                System.out.println("___________________________");

                Object[] usuario = {resultado.getString("id"), resultado.getString("nombre"), resultado.getString("correo")};

                model.addRow(usuario);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }


